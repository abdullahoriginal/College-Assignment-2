#include<iostream>
using namespace std;

int main() {

    // We can find the size of any datatype in C++ using the sizeof() Operator
    cout << "The Size of Int Datatype int is " << sizeof(int) << "bytes" << endl;
    cout << "The Size of Char Datatype int is " << sizeof(char) << "bytes" << endl;
    cout << "The Size of Float Datatype int is " << sizeof(float) << "bytes" << endl;
    cout << "The Size of Double Datatype int is " << sizeof(double) << "bytes" << endl;
    cout << "The Size of Logical Datatype int is " << sizeof(bool) << "bytes" << endl;

    return 0;
    
}